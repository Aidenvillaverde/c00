#include <unistd.h>

void	ft_print_comb(void)
{
	int	x = 0;
	int y = 1;
	int w = 2;

	while (x <= 9)
	{
		char comb[4];
		comb[0] = '0' + x;
		comb[1] = '0' + y;
		comb[2] = '0' + w,
		comb[3] = ',' ;
		write(1, comb, 4);

		w++;
		if	(w > 9)
		{
			y++;
			w = y + 1;
		}
		if	(y > 8)
		{
			x++;
			y = x + 1;
			w =	y + 1;
		}
	}
	write(1, "\n", 1);	
}
